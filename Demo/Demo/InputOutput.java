package Demo;

public class InputOutput {
    public static void main(String args[]){
        // a is a variable(identifier) and int,static,if,for,void, public -- Keyword (Holds special meaning to the compiler and can't be used as a variable/identifier)

        // identifier k rules (naming convention)
        // will not start from a digit or a special character (123/*&^%$# */)

        // = is called assignment operator
        int a = 100;
        float b = 200; // 200.0
        char ch = 'B';
        String name = "Mayank";
        Boolean isTrue = true;

        // a = 10 and b = 20  // Cascading or concatinating
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("ch = " + ch);
        System.out.println(" name = " + name);
        System.out.println("isTrue = "+isTrue);


     


        
    }
}


